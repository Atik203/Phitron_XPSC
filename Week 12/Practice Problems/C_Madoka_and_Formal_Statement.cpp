void solve()
{
    ll n, i, j, f, diff;
    cin >> n;

    ll a[n], b[n];

    for (i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (i = 0; i < n; i++)
    {
        cin >> b[i];
    }
    f = 0;
    for (i = 0; i < n; i++)
    {
        if (a[i] > b[i])
        {
            f++;
            break;
        }
        else
        {
            if (a[i] < b[i])
            {
                if (b[i] - 1 <= b[(i + 1) % n])
                {
                    // no problem
                    ;
                }
                else
                {
                    f++;
                    break;
                }
            }
        }
    }

    if (f == 0)
        cout << "YES\n";
    else
        cout << "NO\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
